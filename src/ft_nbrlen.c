#include "libftprintf.h"

int8_t		ft_nbrlen(intmax_t src)
{
	int8_t		ret;

	if (src == 0)
		return (1);
	ret = (src < 0) ? 1 : 0;
	while (src != 0)
	{
		src = src / 10;
		++ret;
	}
	return (ret);
}
